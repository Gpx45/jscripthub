
// variables are defined by the keyword "var"

// You can NOT create a var beginning with a number.

var myvariable;
var MyVariable;
var my_variable;

// You can NOT use hyphens to create variables.

var myVar = "Hello";

// JS will let you change the type of the variable, unlike other languages. This is known as dynamic types.
// Therefore because of this rule, JS is known as a weakly typed language, because you do not have define a variable when you create it.
var customerAge = 32

customerAge = "thirty-two"

